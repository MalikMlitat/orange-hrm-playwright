    page.get_by_role("textbox", name="Last Name").fill("A")
    page.get_by_role("textbox", name="Last Name").press("CapsLock")
    page.get_by_role("textbox", name="Last Name").fill("Abukwaik")
    page.get_by_role("textbox").nth(4).click()
    page.get_by_role("textbox").nth(4).click()
    page.get_by_role("textbox").nth(4).fill("0422")

    page.get_by_role("button").nth(4).click()
